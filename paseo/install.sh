setup_shell_source() {
  local snippet='for __f in ${PASEO_CACHE:-/home/paseo/cache}/*/env.sh; do [ -r "$__f" ] && . "$__f"; done'
  local rc="$HOME/.profile"
  touch "$rc" 2>/dev/null || return 0
  grep -q 'cache/\*/env.sh' "$rc" 2>/dev/null && return 0
  printf '\n# paseo coding tool environment variables (source each tool dir'"'"'s env.sh)\n%s\n' "$snippet" >> "$rc"
}

usage() {
  cat <<'USAGE'
Usage: install.sh <subcommand> [options]

  Run once at build time (as root):
    setup-bash-wrap   wrap /usr/bin/bash so it sources each tool's env.sh on startup

  Install coding environments on demand at runtime:
     go            Go toolchain
     rust          Rust toolchain
     java          Java (Temurin JDK)
     bun           Bun JS/TS runtime
     scala         Scala (depends on java, auto-installs JDK first)
     kotlin        Kotlin (depends on java, auto-installs JDK first)
     node-tools    TypeScript etc. npm global packages
     lua           Lua 5.4 (official source build)
     php           PHP official source build, minimal CLI

  Install Agent CLI packages on demand at runtime:
     claude-code   Anthropic Claude Code (npm --prefix)
     codex         OpenAI Codex (npm --prefix)
     opencode      OpenCode AI (npm --prefix)

  Options:
     --update, -u   force reinstall / update to the latest

  Examples:
   install.sh setup-bash-wrap     # only used at image build time
   install.sh go rust             # install Go + Rust
   install.sh claude-code opencode # install Claude Code + OpenCode
   install.sh claude-code codex opencode --update  # upgrade all three agents
   install.sh go --update         # update Go to latest
USAGE
}

# Subcommand dispatch
case "${1:-}" in
  setup-bash-wrap)
    setup_bash_wrap
    exit 0
    ;;
  -h|--help|help|"")
    usage
    exit 0
    ;;
esac

# Runtime install logic
TARGETS=()
for a in "$@"; do
  case "$a" in
    --update|-u) ;;
    -h|--help|help) usage; exit 0 ;;
    *) TARGETS+=("$a") ;;
  esac
done

if [ "${#TARGETS[@]}" -eq 0 ]; then
  usage
  exit 0
fi

for t in "${TARGETS[@]}"; do
  case "$t" in
     go)            install_go ;;
     rust)          install_rust ;;
     java)          install_java ;;
     bun)           install_bun ;;
     scala)         install_scala ;;
     kotlin)        install_kotlin ;;
     node-tools|ts) install_node_tools ;;
     lua)          install_lua ;;
     php)          install_php ;;
     claude-code)  install_claude_code ;;
     codex)        install_codex ;;
     opencode)     install_opencode ;;
      *) echo "Unknown target: $t" >&2; usage; exit 1 ;;
  esac
done

setup_shell_source

log "Done. Each tool's environment variables were written to its install dir's env.sh (e.g. cache/go/env.sh),"
log "and the import statement was written to ~/.profile. Effective immediately inside the paseo process (via the bash wrapper); the interactive shell picks it up on reopen."
