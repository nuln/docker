# ---- Helper: link npm bin entries after --prefix install ----
link_npm_bins() {
  local dir="$1"
  mkdir -p "$dir/bin"
  for pkg_json in "$dir/node_modules/"*/package.json "$dir/node_modules/"@*/*/package.json; do
    [ -f "$pkg_json" ] || continue
    local pkg_dir
    pkg_dir="$(dirname "$pkg_json")"
    local bins
    bins=$(node -e "
      const p = require('$pkg_json');
      const b = p.bin;
      if (!b) process.exit(0);
      if (typeof b === 'string') console.log(Object.keys(p.bin || {'dummy':1})[0] + '|' + b);
      else Object.entries(b).forEach(([k,v]) => console.log(k + '|' + v));
    " 2>/dev/null) || continue
    echo "$bins" | while IFS='|' read -r name relpath; do
      [ -z "$name" ] && continue
      local src="$pkg_dir/$relpath"
      if [ -f "$src" ]; then
        ln -sf "$src" "$dir/bin/$name"
        chmod +x "$dir/bin/$name"
      fi
    done
  done
}

usage() {
  cat <<'USAGE'
Usage: install.sh <subcommand> [options]

  Run once at build time:
    setup-shell-source   write env.sh sourcing hook into ~/.profile

  Install coding environments on demand at runtime:
     go            Go toolchain
     rust          Rust toolchain
     node-tools    TypeScript etc. npm global packages

  Install Agent CLI packages on demand at runtime:
     lunel-cli     Lunel CLI
     opencode      OpenCode AI
     codex         OpenAI Codex
     pi            Pi AI

  Options:
     --update, -u   force reinstall / update to the latest

  Examples:
   install.sh setup-shell-source         # only used at image build time
   install.sh go rust                     # install Go + Rust
   install.sh lunel-cli opencode          # install Lunel CLI + OpenCode
   install.sh opencode codex pi --update          # upgrade all three agents
USAGE
}

# Subcommand dispatch
case "${1:-}" in
  setup-shell-source)
    setup_shell_source
    exit 0
    ;;
  -h|--help|help|"")
    usage
    exit 0
    ;;
esac

# Runtime install logic
TARGETS=()
for a in "$@"; do
  case "$a" in
    --update|-u) ;;
    -h|--help|help) usage; exit 0 ;;
    *) TARGETS+=("$a") ;;
  esac
done

if [ "${#TARGETS[@]}" -eq 0 ]; then
  usage
  exit 0
fi

for t in "${TARGETS[@]}"; do
  case "$t" in
     go)            install_go ;;
     rust)          install_rust ;;
     node-tools|ts) install_node_tools ;;
     lunel-cli)     install_lunel_cli ;;
     opencode)      install_opencode ;;
     codex)         install_codex ;;
     pi)            install_pi ;;
      *) echo "Unknown target: $t" >&2; usage; exit 1 ;;
  esac
done

setup_shell_source

log "Done. Each tool's environment variables were written to its install dir's env.sh,"
log "and the import statement was written to ~/.profile."
